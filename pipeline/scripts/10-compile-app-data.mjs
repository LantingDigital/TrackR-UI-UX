#!/usr/bin/env node
// Phase 10: Compile pipeline data into TypeScript modules for the app.
//
// Reads all coaster + park JSON files and produces:
//   1. src/data/coasterIndex.ts   — All coasters, compact (search + basic stats)
//   2. src/data/coasterDetails.ts — Top 200 iconic coasters with rich detail
//   3. src/data/parkIndex.ts      — All parks, compact
//   4. src/data/trendingData.ts   — Pre-computed trending & popular lists
//
// Usage:
//   node scripts/10-compile-app-data.mjs

import { resolve, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';
import { readdirSync, readFileSync, writeFileSync } from 'node:fs';

const __dirname = dirname(fileURLToPath(import.meta.url));
const DATA_DIR = resolve(__dirname, '../data');
const COASTERS_DIR = resolve(DATA_DIR, 'coasters');
const PARKS_DIR = resolve(DATA_DIR, 'parks');
const RANKINGS_PATH = resolve(DATA_DIR, 'popularity-rankings.json');
const SRC_DATA_DIR = resolve(__dirname, '../../src/data');

// ── Helpers ─────────────────────────────────────────────────

function capitalize(s) {
  if (!s) return s;
  return s.charAt(0).toUpperCase() + s.slice(1);
}

function escapeTs(s) {
  if (typeof s !== 'string') return s;
  return s.replace(/\\/g, '\\\\').replace(/'/g, "\\'").replace(/\n/g, '\\n');
}

function readJson(path) {
  return JSON.parse(readFileSync(path, 'utf-8'));
}

// ── Load all coaster base files (not .wiki.json or .cp-wiki.json) ──

function loadCoasters() {
  const files = readdirSync(COASTERS_DIR)
    .filter(f => f.endsWith('.json') && !f.includes('.wiki.') && !f.includes('.cp-wiki.'));

  console.log(`  Loading ${files.length} coaster files...`);
  const coasters = [];

  for (const file of files) {
    try {
      const data = readJson(resolve(COASTERS_DIR, file));
      coasters.push(data);
    } catch (e) {
      console.warn(`  Warning: Could not read ${file}: ${e.message}`);
    }
  }

  return coasters;
}

// ── Load all park base files (not .wiki.json) ──

function loadParks() {
  const files = readdirSync(PARKS_DIR)
    .filter(f => f.endsWith('.json') && !f.includes('.wiki.'));

  console.log(`  Loading ${files.length} park files...`);
  const parks = [];

  for (const file of files) {
    try {
      const data = readJson(resolve(PARKS_DIR, file));
      parks.push(data);
    } catch (e) {
      console.warn(`  Warning: Could not read ${file}: ${e.message}`);
    }
  }

  return parks;
}

// ── Map pipeline coaster → app format ──

function mapCoasterIndex(c) {
  return {
    id: c.id,
    name: c.name,
    park: c.parkName || '',
    parkId: c.parkId || '',
    country: c.countryName || c.country || '',
    continent: c.continent || '',
    manufacturer: c.manufacturer || '',
    material: c.material || 'Steel',
    type: c.type || '',
    heightFt: c.heightFt || 0,
    speedMph: c.speedMph || 0,
    lengthFt: c.lengthFt || 0,
    inversions: c.inversions || 0,
    yearOpened: c.yearOpened || 0,
    dropFt: c.dropFt || undefined,
    gForce: c.gForce || undefined,
    duration: c.duration || undefined,
    propulsion: c.propulsion || undefined,
    designer: c.designer || undefined,
    model: c.model || undefined,
    status: capitalize(c.status) || undefined,
    imageUrl: c.imageUrl || undefined,
    popularityScore: c.popularityScore || 0,
    popularityRank: c.popularityRank || 99999,
  };
}

function mapCoasterDetail(c) {
  return {
    description: c.description || undefined,
    notableFeatures: c.notableFeatures?.length ? c.notableFeatures : undefined,
    records: c.records?.length ? c.records : undefined,
    wikiUrl: c.wikiUrl || undefined,
    formerNames: c.formerNames?.length ? c.formerNames : undefined,
  };
}

function mapParkIndex(p) {
  return {
    id: p.id,
    name: p.name,
    country: p.countryName || p.country || '',
    region: p.region || '',
    city: p.city || '',
    coasterCount: p.coasterCount || 0,
    status: capitalize(p.status) || undefined,
    imageUrl: p.imageUrl || undefined,
    owner: p.owner || undefined,
    wikiUrl: p.wikiUrl || undefined,
  };
}

// ── Generate TypeScript ─────────────────────────────────────

function genCoasterIndex(coasters) {
  const mapped = coasters.map(mapCoasterIndex);
  // Sort by popularity rank (lower is better)
  mapped.sort((a, b) => a.popularityRank - b.popularityRank);

  // Write JSON data separately (avoids TS literal analysis of 3000 objects)
  const jsonPath = resolve(SRC_DATA_DIR, 'coasterIndex.json');
  writeFileSync(jsonPath, JSON.stringify(mapped), 'utf-8');

  const ts = `// AUTO-GENERATED by pipeline/scripts/10-compile-app-data.mjs
// Do not edit manually. Re-run the script to update.
// Generated: ${new Date().toISOString()}
// Total coasters: ${mapped.length}

import rawCoasterData from './coasterIndex.json';

export interface CoasterIndexEntry {
  id: string;
  name: string;
  park: string;
  parkId: string;
  country: string;
  continent: string;
  manufacturer: string;
  material: string;
  type: string;
  heightFt: number;
  speedMph: number;
  lengthFt: number;
  inversions: number;
  yearOpened: number;
  dropFt?: number;
  gForce?: number;
  duration?: number;
  propulsion?: string;
  designer?: string;
  model?: string;
  status?: string;
  imageUrl?: string;
  popularityScore: number;
  popularityRank: number;
}

export const COASTER_INDEX: CoasterIndexEntry[] = rawCoasterData as CoasterIndexEntry[];

// Lookup map keyed by coaster ID
export const COASTER_BY_ID: Record<string, CoasterIndexEntry> = {};
for (const c of COASTER_INDEX) {
  COASTER_BY_ID[c.id] = c;
}
`;

  return ts;
}

function genCoasterDetails(coasters, limit = 200) {
  // Take top coasters by popularity that have descriptions
  const withDetails = coasters
    .filter(c => c.description || (c.notableFeatures && c.notableFeatures.length) || (c.records && c.records.length))
    .sort((a, b) => (a.popularityRank || 99999) - (b.popularityRank || 99999))
    .slice(0, limit);

  let ts = `// AUTO-GENERATED by pipeline/scripts/10-compile-app-data.mjs
// Do not edit manually. Re-run the script to update.
// Generated: ${new Date().toISOString()}
// Coasters with rich details: ${withDetails.length}

export interface CoasterDetailData {
  description?: string;
  notableFeatures?: string[];
  records?: string[];
  wikiUrl?: string;
  formerNames?: string[];
}

export const COASTER_DETAILS: Record<string, CoasterDetailData> = {\n`;

  for (const c of withDetails) {
    const detail = mapCoasterDetail(c);
    // Only include if there's something meaningful
    const hasContent = detail.description || detail.notableFeatures || detail.records;
    if (!hasContent) continue;
    ts += `  '${escapeTs(c.id)}': ${JSON.stringify(detail)},\n`;
  }

  ts += '};\n';
  return ts;
}

function genParkIndex(parks) {
  const mapped = parks.map(mapParkIndex);
  // Sort by coaster count descending
  mapped.sort((a, b) => b.coasterCount - a.coasterCount);

  // Write JSON data separately (avoids TS literal analysis of 1200+ objects)
  const jsonPath = resolve(SRC_DATA_DIR, 'parkIndex.json');
  writeFileSync(jsonPath, JSON.stringify(mapped), 'utf-8');

  const ts = `// AUTO-GENERATED by pipeline/scripts/10-compile-app-data.mjs
// Do not edit manually. Re-run the script to update.
// Generated: ${new Date().toISOString()}
// Total parks: ${mapped.length}

import rawParkData from './parkIndex.json';

export interface ParkIndexEntry {
  id: string;
  name: string;
  country: string;
  region: string;
  city: string;
  coasterCount: number;
  status?: string;
  imageUrl?: string;
  owner?: string;
  wikiUrl?: string;
}

export const PARK_INDEX: ParkIndexEntry[] = rawParkData as ParkIndexEntry[];

// Lookup map keyed by park ID
export const PARK_BY_ID: Record<string, ParkIndexEntry> = {};
for (const p of PARK_INDEX) {
  PARK_BY_ID[p.id] = p;
}
`;

  return ts;
}

function genTrendingData(coasters, parks) {
  // Top 10 trending coasters by popularity score
  const trending = coasters
    .filter(c => c.popularityScore && c.popularityScore > 0)
    .sort((a, b) => (b.popularityScore || 0) - (a.popularityScore || 0))
    .slice(0, 10)
    .map((c, i) => ({
      rank: i + 1,
      id: c.id,
      name: c.name,
      park: c.parkName || '',
      score: c.popularityScore || 0,
      imageUrl: c.imageUrl || undefined,
    }));

  // Top 20 popular rides for carousel (operating only, with images preferred)
  const popularRides = coasters
    .filter(c => (c.status === 'operating' || c.status === 'Operating') && c.popularityScore > 0)
    .sort((a, b) => (b.popularityScore || 0) - (a.popularityScore || 0))
    .slice(0, 20)
    .map(c => ({
      id: c.id,
      name: c.name,
      park: c.parkName || '',
      imageUrl: c.imageUrl || undefined,
    }));

  // Top 10 parks by coaster count (operating only)
  const popularParks = parks
    .filter(p => (p.status === 'operating' || p.status === 'Operating') && p.coasterCount > 0)
    .sort((a, b) => (b.coasterCount || 0) - (a.coasterCount || 0))
    .slice(0, 10)
    .map(p => ({
      id: p.id,
      name: p.name,
      city: p.city || p.region || '',
      country: p.countryName || p.country || '',
      coasterCount: p.coasterCount || 0,
      imageUrl: p.imageUrl || undefined,
    }));

  let ts = `// AUTO-GENERATED by pipeline/scripts/10-compile-app-data.mjs
// Do not edit manually. Re-run the script to update.
// Generated: ${new Date().toISOString()}

export interface TrendingCoasterEntry {
  rank: number;
  id: string;
  name: string;
  park: string;
  score: number;
  imageUrl?: string;
}

export interface PopularRideEntry {
  id: string;
  name: string;
  park: string;
  imageUrl?: string;
}

export interface PopularParkEntry {
  id: string;
  name: string;
  city: string;
  country: string;
  coasterCount: number;
  imageUrl?: string;
}

export const TRENDING_COASTERS: TrendingCoasterEntry[] = ${JSON.stringify(trending, null, 2)};

export const POPULAR_RIDES: PopularRideEntry[] = ${JSON.stringify(popularRides, null, 2)};

export const POPULAR_PARKS: PopularParkEntry[] = ${JSON.stringify(popularParks, null, 2)};
`;

  return ts;
}

// ── Main ────────────────────────────────────────────────────

console.log('=== TrackR App Data Compiler ===\n');

console.log('Phase 1: Loading pipeline data...');
const coasters = loadCoasters();
const parks = loadParks();
console.log(`  Loaded ${coasters.length} coasters, ${parks.length} parks\n`);

console.log('Phase 2: Generating coasterIndex.ts...');
const coasterIndexTs = genCoasterIndex(coasters);
writeFileSync(resolve(SRC_DATA_DIR, 'coasterIndex.ts'), coasterIndexTs, 'utf-8');
console.log(`  Written (${(coasterIndexTs.length / 1024).toFixed(0)} KB)\n`);

console.log('Phase 3: Generating coasterDetails.ts...');
const coasterDetailsTs = genCoasterDetails(coasters, 200);
writeFileSync(resolve(SRC_DATA_DIR, 'coasterDetails.ts'), coasterDetailsTs, 'utf-8');
console.log(`  Written (${(coasterDetailsTs.length / 1024).toFixed(0)} KB)\n`);

console.log('Phase 4: Generating parkIndex.ts...');
const parkIndexTs = genParkIndex(parks);
writeFileSync(resolve(SRC_DATA_DIR, 'parkIndex.ts'), parkIndexTs, 'utf-8');
console.log(`  Written (${(parkIndexTs.length / 1024).toFixed(0)} KB)\n`);

console.log('Phase 5: Generating trendingData.ts...');
const trendingTs = genTrendingData(coasters, parks);
writeFileSync(resolve(SRC_DATA_DIR, 'trendingData.ts'), trendingTs, 'utf-8');
console.log(`  Written (${(trendingTs.length / 1024).toFixed(0)} KB)\n`);

console.log('=== Done! ===');
console.log(`  coasterIndex.ts  — ${coasters.length} coasters`);
console.log(`  coasterDetails.ts — top 200 with rich details`);
console.log(`  parkIndex.ts     — ${parks.length} parks`);
console.log(`  trendingData.ts  — trending & popular lists`);
