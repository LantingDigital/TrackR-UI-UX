// ---- Search Helpers ----

/** Search all food locations by menu item keyword */
export function searchMenuItems(query: string): ParkPOI[] {
  const q = query.toLowerCase().trim();
  if (!q) return [];
  return KNOTTS_POI.filter(
    (poi) =>
      poi.type === 'food' &&
      poi.menuItems?.some((item) => item.toLowerCase().includes(q)),
  );
}

/** Filter POIs by type */
export function filterByType(type: POIType): ParkPOI[] {
  return KNOTTS_POI.filter((poi) => poi.type === type);
}

/** Filter POIs by area */
export function filterByArea(area: ParkArea): ParkPOI[] {
  return KNOTTS_POI.filter((poi) => poi.area === area);
}

/** Filter rides by max height (find rides a child can go on) */
export function ridesForHeight(heightInches: number): ParkPOI[] {
  return KNOTTS_POI.filter((poi) => {
    if (poi.type !== 'ride') return false;
    if (!poi.heightRequirement) return true; // no requirement = anyone can ride
    const req = poi.heightRequirement;
    // Can ride solo
    if (req.min && heightInches >= req.min) return true;
    // Can ride with companion
    if (req.withCompanion && heightInches >= req.withCompanion) return true;
    // No min specified = can ride
    if (!req.min && !req.withCompanion) return true;
    return false;
  });
}

